#include <stdio.h>

int	ft_atoi(char *str)
{
	int	result;
	int	sign;
	int	i;
	int	counter;

	result = 0;
	sign = 1;
	i = 0;
	counter = 0;
	while (str[i] <= ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			counter++;
		i++;
	}
	if (counter % 2 != 0)
		sign = -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (sign * result);
}
